int TikFinder::GetNearestColors(int r, int g, int b,LPCTSTR prior)
{
	for(int i=0;i<m_arr_RGBs.GetSize();i++)
	{
		COLORREF cr = m_arr_RGBs[i].color;
		int r1 = GetRValue(cr);
		int g1 = GetGValue(cr);
		int b1 = GetBValue(cr);
		m_arr_RGBs[i].sum = sqrt((float)((r-r1)*(r-r1)+(g-g1)*(g-g1)+(b-b1)*(b-b1)));

		m_arr_RGBs[i].sumr = abs(r-r1);
		m_arr_RGBs[i].sumg = abs(g-g1);
		m_arr_RGBs[i].sumb = abs(b-b1);
	}
	qsort_s(m_arr_RGBs.GetData(),m_arr_RGBs.GetSize(),sizeof(RGB_NEAREST),compare_colors,(void*)&CString(prior));

	return 0;
}

void TikFinder::Fill3DMap(void)
{
	int iteration = 0;
	int pointsleft = 256*256*256-(int)m_arr_RGBs.GetSize();
	
	while(m_list_colors.IsEmpty() == FALSE)
	{
		COLORREF cr_base = m_list_colors.RemoveHead();
		iteration++;
		
		int r = GetRValue(cr_base);
		int g = GetGValue(cr_base);
		int b = GetBValue(cr_base);

		ASSERT(m_tik_map[r][g][b]!=0);
		for(int r1=-1;r1<=1;r1++)
			for(int g1=-1;g1<=1;g1++)
				for(int b1=-1;b1<=1;b1++)
				{
					int rt = max(r+r1,0);
					int gt = max(g+g1,0);
					int bt = max(b+b1,0);
					rt = min(rt,255);
					gt = min(gt,255);
					bt = min(bt,255);
					if(m_tik_map[rt][gt][bt] == 0)
					{
						m_tik_map[rt][gt][bt] = m_tik_map[r][g][b];
						m_list_colors.AddTail(RGB(rt,gt,bt));
						pointsleft--;
					}
				}
		
		
	}

	int len = sizeof(m_tik_map);
	CFile file;
	file.Open("C:\\arrtik.bin",CFile::modeWrite|CFile::modeCreate);
	file.Write(m_tik_map,sizeof(m_tik_map));
}
