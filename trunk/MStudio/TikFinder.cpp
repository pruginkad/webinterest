int compare_colors( void *crbase, const void *cr1, const void *cr2)
{
	RGB_NEAREST* pcr1 = (RGB_NEAREST*)cr1;
	RGB_NEAREST* pcr2 = (RGB_NEAREST*)cr2;
	CString* pPrior = (CString*)crbase;

	if(*pPrior == "r")
	{
		if(pcr1->sumr < pcr2->sumr)
			return -1;
		if(pcr1->sumr > pcr2->sumr)
			return 1;
	}

	if(*pPrior == "g")
	{
		if(pcr1->sumg < pcr2->sumg)
			return -1;
		if(pcr1->sumg > pcr2->sumg)
			return 1;
	}

	if(*pPrior == "b")
	{
		if(pcr1->sumb < pcr2->sumb)
			return -1;
		if(pcr1->sumb > pcr2->sumb)
			return 1;
	}

	if(pcr1->sum < pcr2->sum)
		return -1;
	if(pcr1->sum > pcr2->sum)
		return 1;

	return 0;
}

